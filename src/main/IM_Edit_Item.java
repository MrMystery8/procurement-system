   private void loadTable() {
      String[] columnNames = {"Item ID", "Name", "Supplier ID", "Stock Level", "Reorder Level", "Price", "Cost"};
    tablemodel = new DefaultTableModel(columnNames, 0); // Initialize table model
    
    String[] items = Item.getAllItems(); 
    if (items != null) {
        for (String itemLine : items) {
            String[] itemDetails = itemLine.split(";");
            if (itemDetails.length >= 7) { // Check for valid data length
                String[] rowData = {
                    itemDetails[0], 
                    itemDetails[1],
                    itemDetails[2], 
                    itemDetails[3], 
                    itemDetails[4], 
                    itemDetails[5], 
                    itemDetails[6]
                };
                tablemodel.addRow(rowData);
            } else {
                System.out.println("Invalid data: " + itemLine);
            }
        }
    } else {
        System.out.println("No items returned.");
    }
    
    // Set the updated model to the table
    ItemsTable1.setModel(tablemodel);
    }
   
