    private void loadSupplierData() {
     try {
        // Open the suppliers.txt file
        File file = new File("suppliers.txt");
        BufferedReader br = new BufferedReader(new FileReader(file));

        String line;
        cbSupplier1.removeAllItems(); // Clear existing items in the combo box

        // Read each line of the file
        while ((line = br.readLine()) != null) {
            // Split the line using the delimiter (e.g., semicolon)
            String[] supplierData = line.split(";");
            if (supplierData.length > 0) {
                // Add only the supplier ID (assumed to be the first part)
                cbSupplier1.addItem(supplierData[0].trim());
            }
        }
        br.close(); // Close the BufferedReader
    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Error loading supplier data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}

