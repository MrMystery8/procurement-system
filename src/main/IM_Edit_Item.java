    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
         String itemID = tfUsername1.getText();

    if (itemID.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please select an item to delete.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    int confirmation = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this item?", "Confirm Deletion", JOptionPane.YES_NO_OPTION);

    if (confirmation == JOptionPane.YES_OPTION) {
        boolean success = manager.deleteItem(itemID);

        if (success) {
            JOptionPane.showMessageDialog(this, "Item deleted successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            loadTable(); // Reload the table
            clearFields();
        } else {
            JOptionPane.showMessageDialog(this, "Item deletion failed. Item may not exist.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnEdit1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEdit1ActionPerformed
          try {
        // Validate if any text fields are empty
        if (tfUsername1.getText().isEmpty() || 
            tfName1.getText().isEmpty() || 
            tfStockLvl1.getText().isEmpty() || 
            tfReorderLvl1.getText().isEmpty() || 
            tfPrice.getText().isEmpty() || 
            tfCost.getText().isEmpty()) {
            
            JOptionPane.showMessageDialog(this, "Please fill in all fields!", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Parse and validate numeric fields
        int stockLevel;
        int reorderLevel;
        double price;
        double cost;

        try {
            stockLevel = Integer.parseInt(tfStockLvl1.getText().trim());
            reorderLevel = Integer.parseInt(tfReorderLvl1.getText().trim());
            price = Double.parseDouble(tfPrice.getText().trim());
            cost = Double.parseDouble(tfCost.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Stock Level, Reorder Level, Price, and Cost must be valid numbers!", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Check for negative values
        if (stockLevel < 0 || reorderLevel < 0 || price < 0 || cost < 0) {
            JOptionPane.showMessageDialog(this, "Numeric values must be positive numbers!", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Get remaining inputs
        String itemID = tfUsername1.getText().trim();
        String name = tfName1.getText().trim();
        String supplierID = cbSupplier1.getSelectedItem().toString();

        // Attempt to update the item
        boolean success = manager.editItem(itemID, name, supplierID, stockLevel, reorderLevel, price, cost);

        if (success) {
            JOptionPane.showMessageDialog(this, "Item updated successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            loadTable(); // Reload the table with updated data
            clearFields(); // Clear the input fields
        } else {
            JOptionPane.showMessageDialog(this, "Item update failed. Please check the details.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "An unexpected error occurred: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnEdit1ActionPerformed

