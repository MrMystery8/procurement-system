    private void loadSupplierTable() {
     if (tableModel == null) {
        // Initialize the table model once
        String[] columnNames = {"Supplier ID", "Name", "Contact", "Location"};
        tableModel = new DefaultTableModel(columnNames, 0);
        SupplierTable.setModel(tableModel); // Set the model to the table
    }

    // Clear existing rows without reinitializing
    tableModel.setRowCount(0);

    // Retrieve all suppliers
    List<Supplier> suppliers = manager.getAllSuppliers();
    if (suppliers != null && !suppliers.isEmpty()) {
        for (Supplier supplier : suppliers) {
            String[] rowData = {
                supplier.getSupplierID(),
                supplier.getName(),
                supplier.getContact(),
                supplier.getLocation()
            };
            tableModel.addRow(rowData); // Add each supplier's data as a row
        }
    } else {
        System.out.println("No suppliers returned.");
    }
    }
    
