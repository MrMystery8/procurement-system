    private void SupplierTableMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SupplierTableMouseReleased
          int row = SupplierTable.rowAtPoint(evt.getPoint());
    
    
    if (row >= 0) {
        
        tfSupplierID.setText(SupplierTable.getValueAt(row, 0).toString()); 
        tfName1.setText(SupplierTable.getValueAt(row, 1).toString());     
        tfContact.setText(SupplierTable.getValueAt(row, 2).toString());   
        tfLocation.setText(SupplierTable.getValueAt(row, 3).toString());  

        tfSupplierID.setEditable(false);
    }
    }//GEN-LAST:event_SupplierTableMouseReleased

